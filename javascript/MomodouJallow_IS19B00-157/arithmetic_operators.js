let x = 10;
let y = 3;

// Addition operator(+)
console.log(x + y);

// Subtraction operator(-)
console.log(x - y)

// Multiplication operator(*)
console.log(x * y)

// Division operator(/)
console.log(x / y)

// Modulus operator(%)- returns the remainder of division
console.log(x % y)

// Exponentiation operator(**)
console.log(x ** y)

// Increment operator(++)
console.log(++x)  // the value of x is incremented by 1

// Decrement operator(--)
console.log(--x)  // the value of x is decremented by 1







