/** RULES FOR NAMING VARIABLES IN JAVASCRIPT
 
Variable names cannot be reserved keywords such as
  -let
  -if
  -else
  -var etc

They cannot start with a number

They cannot contain a space or hyphen

Use meaningful and desriptive names 

We use Camel Case notation as convention for naming variables in JavaSript

Declare each variable on a separate
 */

let FirstName = 'Galleh';
console.log(FirstName);

let x = 10;
let y = 2;
let sum = x + y;
console.log(sum);
