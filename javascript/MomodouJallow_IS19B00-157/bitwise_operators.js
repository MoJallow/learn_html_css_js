// 1 = 00000001
// 2 = 00000010
// 3 = 00000011

// Bitwise OR(|) operator
console.log(1 | 2) // Returns 3


// Bitwise AND (&) Operator
console.log(1 & 2)  //Returns 0

// Bitwise XOR(^) operator
console.log(1 ^ 2)  // Returns 3

// Bitwise NOT(~) operator
let x = 12;
console.log(~x)  // Returns -13