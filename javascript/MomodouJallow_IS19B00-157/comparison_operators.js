let x = 1;

// Relational operators
console.log(x > 0);  // Greater than operator
console.log(x < 0);  // Less than operator
console.log(x >= 1);  // Greater than or equal to operator
console.log(x <= 10);  // Less than or equal to operator

// Equality operators
console.log(x === 1);  // Strict equality operator
console.log(x !== 1);  // Not equal to operator
